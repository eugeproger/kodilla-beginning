package com.kodilla.modul0.submodul4;

public class OperationsOnVariables {
    public static void main(String[] args) {
        int numberOne = 25;
        double numberTwo = 6;
        int numberThree = 10;
        double numberFour = 5.5;

        double sum = numberOne + numberTwo;
        double minus = numberOne - numberTwo;
        double multiplication = numberThree * numberFour;
        double division = numberThree / numberFour;
        double modulo = numberOne % numberTwo;

        System.out.println(sum);
        System.out.println(minus);
        System.out.println(multiplication);
        System.out.println(division);
        System.out.println(modulo);
    }
}
